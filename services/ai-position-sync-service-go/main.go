package main

import (
	"context"
	"database/sql"
	"flag"
	"log/slog"
	"net/http"
	"os"
	"os/signal"
	"syscall"
	"time"

	_ "github.com/lib/pq" // PostgreSQL driver

	"necpgame/services/ai-position-sync-service-go/server"
)

func main() {
	var (
		addr     = flag.String("addr", ":8080", "HTTP server address")
		dbURL    = flag.String("db-url", "postgres://user:password@localhost:5432/necpdb?sslmode=disable", "PostgreSQL connection URL")
		logLevel = flag.String("log-level", "info", "Log level (debug, info, warn, error)")
		redisURL = flag.String("redis-url", "redis://localhost:6379", "Redis connection URL")
	)
	flag.Parse()

	// Setup structured logging
	logger := setupLogging(*logLevel)
	slog.SetDefault(logger)

	slog.Info("Starting AI Position Sync Service",
		"addr", *addr,
		"version", "1.0.0",
	)

	// Initialize database connection with performance optimizations
	db, err := initDatabase(*dbURL)
	if err != nil {
		slog.Error("Failed to initialize database", "error", err)
		os.Exit(1)
	}
	defer db.Close()

	// Initialize repository
	repo, err := server.NewPostgresRepository(db)
	if err != nil {
		slog.Error("Failed to initialize repository", "error", err)
		os.Exit(1)
	}

	// Initialize Redis client for real-time position sync
	redisClient, err := initRedis(*redisURL)
	if err != nil {
		slog.Error("Failed to initialize Redis", "error", err)
		os.Exit(1)
	}
	defer redisClient.Close()

	// Initialize service with performance optimizations
	service := server.NewAiPositionSyncService(repo, redisClient)

	// Initialize metrics
	metrics := server.NewServiceMetrics()

	// Initialize middleware
	middleware := server.NewMiddleware()

	// Setup HTTP server with performance-critical timeouts
	httpServer := server.NewHTTPServer(*addr, setupRouter(service, middleware, metrics))

	// Start server
	go func() {
		slog.Info("HTTP server starting", "addr", *addr)
		if err := httpServer.Start(); err != nil && err != http.ErrServerClosed {
			slog.Error("HTTP server failed", "error", err)
			os.Exit(1)
		}
	}()

	// Graceful shutdown
	quit := make(chan os.Signal, 1)
	signal.Notify(quit, syscall.SIGINT, syscall.SIGTERM)
	<-quit

	slog.Info("Shutting down server...")
	ctx, cancel := context.WithTimeout(context.Background(), 30*time.Second)
	defer cancel()

	if err := httpServer.Stop(ctx); err != nil {
		slog.Error("Server forced to shutdown", "error", err)
	}

	slog.Info("Server exited")
}

func setupLogging(level string) *slog.Logger {
	var logLevel slog.Level
	switch level {
	case "debug":
		logLevel = slog.LevelDebug
	case "info":
		logLevel = slog.LevelInfo
	case "warn":
		logLevel = slog.LevelWarn
	case "error":
		logLevel = slog.LevelError
	default:
		logLevel = slog.LevelInfo
	}

	handler := slog.NewTextHandler(os.Stdout, &slog.HandlerOptions{
		Level: logLevel,
	})
	return slog.New(handler)
}

func initDatabase(dbURL string) (*sql.DB, error) {
	db, err := sql.Open("postgres", dbURL)
	if err != nil {
		return nil, err
	}

	// Performance: Database connection pool configuration for high-throughput MMOFPS
	db.SetMaxOpenConns(100)         // Maximum open connections
	db.SetMaxIdleConns(10)          // Maximum idle connections
	db.SetConnMaxLifetime(time.Hour) // Connection max lifetime

	// Test connection
	if err := db.Ping(); err != nil {
		return nil, err
	}

	return db, nil
}

func initRedis(redisURL string) (*redis.Client, error) {
	opt, err := redis.ParseURL(redisURL)
	if err != nil {
		return nil, err
	}

	client := redis.NewClient(opt)

	// Test connection
	if err := client.Ping(context.Background()).Err(); err != nil {
		return nil, err
	}

	return client, nil
}

