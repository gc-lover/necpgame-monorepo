func (h *Handler) AiPositionSyncHealthCheck(w http.ResponseWriter, r *http.Request) {
	ctx, cancel := context.WithTimeout(r.Context(), 5*time.Second)
	defer cancel()

	// Check database connectivity
	if err := h.service.(*AiPositionSyncService).repo.Ping(ctx); err != nil {
		slog.Error("Health check failed: database ping", "error", err)
		http.Error(w, "Service unhealthy: database", http.StatusServiceUnavailable)
		return
	}

	// Check Redis connectivity
	if err := h.service.(*AiPositionSyncService).redis.Ping(ctx).Err(); err != nil {
		slog.Error("Health check failed: Redis ping", "error", err)
		http.Error(w, "Service unhealthy: Redis", http.StatusServiceUnavailable)
		return
	}

	w.WriteHeader(http.StatusOK)
	fmt.Fprint(w, "OK")
}

